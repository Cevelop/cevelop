package com.cevelop.includator.tests.declreftests;

import java.util.List;

import org.junit.Assert;
import org.junit.Test;

import com.cevelop.includator.cxxelement.DeclarationReference;
import com.cevelop.includator.tests.base.IncludatorTest;


public class DeclRefTest9NamespaceAroundMethodDef extends IncludatorTest {

    @Test
    public void runTest() throws Throwable {
        List<DeclarationReference> references = getActiveFileDeclarationReferences();

        Assert.assertEquals((Object) 2, (Object) references.size());

        int i = 0;
        assertDeclarationReference("bar", "A.cpp", references.get(i++));
        assertDeclarationReference("A", "A.cpp", references.get(i++));
    }
}
